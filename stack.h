/* This structure is how we represent a stack. (Very similar to array list). */
typedef struct 
{
  int  top;
  int  slots;
  int* arr;
} stack;


/*******************************************************************************
* Stack functions.
*******************************************************************************/
stack*      newStack();
//------------------------------------------------------------------------------
void        push(stack *s, int e);
//------------------------------------------------------------------------------
int         pop(stack *s);
//------------------------------------------------------------------------------
int         peek(stack *s);
//------------------------------------------------------------------------------
int         stackSize(stack *s);
//------------------------------------------------------------------------------
void        testStack();
//------------------------------------------------------------------------------
void        freeStack(stack *s);
//------------------------------------------------------------------------------
int         isEmpty(stack *s);
//------------------------------------------------------------------------------
void        emptyStack(stack *s);
/******************************************************************************/
